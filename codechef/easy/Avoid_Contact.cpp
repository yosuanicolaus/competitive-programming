#include <bits/stdc++.h>
using namespace std;
using ll = long long;

auto solve() {
    int x, y;
    cin >> x >> y;
    if (x == y) return x * 2 - 1;
    return (x - y) + 2 * y;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--)
        cout << solve() << '\n';
    return 0;
}
